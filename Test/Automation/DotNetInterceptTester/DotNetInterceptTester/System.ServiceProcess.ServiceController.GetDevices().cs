namespace DotNetInterceptTester.My_System.ServiceProcess.ServiceController
{
public class GetDevices
{
public static bool _GetDevices( )
{
   //Parameters

   //ReturnType/Value
   System.ServiceProcess.ServiceController[] returnVal_Real = null;
   System.ServiceProcess.ServiceController[] returnVal_Intercepted = null;

   //Exception
   Exception exception_Real = null;
   Exception exception_Intercepted = null;

   InterceptionMaintenance.disableInterception( );

   try
   {
      returnValue_Real = System.ServiceProcess.ServiceController.GetDevices(;
   }

   catch( Exception e )
   {
      exception_Real = e;
   }


   InterceptionMaintenance.enableInterception( );

   try
   {
      returnValue_Intercepted = System.ServiceProcess.ServiceController.GetDevices(;
   }

   catch( Exception e )
   {
      exception_Intercepted = e;
   }


   Return ( ( exception_Real.Messsage == exception_Intercepted.Message ) && ( returnValue_Real == returnValue_Intercepted ) );
}
}
}
